
;;; All the macros and the scheme-object printing procedure
;;; are defined in compiler.s
%include "compiler.s"

section .bss
;;; This pointer is used to manage allocations on our heap.
malloc_pointer:
    resq 1

section .data

%macro MAKE_LITERAL 2 ; Make a literal of type %1
; followed by the definition %2
db %1
%2
%endmacro

const_tbl:
MAKE_VOID
MAKE_NIL
MAKE_BOOL(0)
MAKE_BOOL(1)
MAKE_LITERAL_INTEGER(1)
MAKE_LITERAL_INTEGER(2)

;;; These macro definitions are required for the primitive
;;; definitions in the epilogue to work properly
%define SOB_VOID_ADDRESS const_tbl+0
%define SOB_NIL_ADDRESS const_tbl+1
%define SOB_FALSE_ADDRESS const_tbl+2
%define SOB_TRUE_ADDRESS const_tbl+4

fvar_tbl:


global main
section .text
main:
    push rbp

    ;; set up the heap
    mov rdi, GB(1)
    call malloc
    mov [malloc_pointer], rax

    ;; Set up the dummy activation frame
    ;; The dummy return address is T_UNDEFINED
    ;; (which a is a macro for 0) so that returning
    ;; from the top level (which SHOULD NOT HAPPEN
    ;; AND IS A BUG) will cause a segfault.
    push 0
    push qword SOB_NIL_ADDRESS
    push qword T_UNDEFINED
    push rsp
    mov rbp,rsp

    ;; Set up the primitive stdlib fvars:
    ;; Since the primtive procedures are defined in assembly,
    ;; they are not generated by scheme (define ...) expressions.
    ;; This is where we emulate the missing (define ...) expressions
    ;; for all the primitive procedures.
;GENERATE APPLIC
gdb:
    mov rax, SOB_NIL_ADDRESS
    push rax 
;GENERATE CONST:
 mov rax, const_tbl+15
 ;<end const> 
push rax 
;GENERATE CONST:
 mov rax, const_tbl+6
 ;<end const> 
push rax 
push 2
;GENERATE FIRST SIMPLE LAMBDA:
MAKE_CLOSURE(rax, SOB_NIL_ADDRESS,lambda_body_1)
    jmp end_lambda_body_1
lambda_body_1:
push rbp
    mov rbp,rsp 
;GENERATE LAMBDA OPT:
mov r8, qword [rbp+8*6]
      mov r8, qword [rbp+8*5] 
      mov r8, qword [rbp+8*4] 
      mov r8, qword [rbp+8*3] 
mov r9, 1
mov r10, r8
      sub r10, r9 ; list size = n - expected args
      cmp r10, 0
      je end_lambda_opt_1
mov rax, r9 ; rax = expected
      add rax, 4 ; rax = expected+4
      shl rax, 3 ; rax = (expected+4)*8
      add rax, rbp ; rax = rbp + (expected+4)*8
      mov r11, [rax] ; r11 points to first opt arg
MALLOC rax, r10 

          mov rcx, r10
          build_opt_list_1:
          mov r12, qword [r11]
          mov [rax], r12
          add r11, 8
          add rax, 8
          loop build_opt_list_1
          mov r11, rax ; first opt arg has been replaced with a pointer to the opt_list
          end_lambda_opt_1:
;end lambda opt>
;GENERATE PARAM GET:
mov rax, qword [rbp + 40] 
 ;<end param get> 

    leave
    ret
end_lambda_body_1: 
 ;<end first simple lambda> 
CLOSURE_ENV r9, rax
    push r9 
CLOSURE_CODE r10, rax

    call r10
add rsp , 8*1 ; pop env
    add rbx, 1
    pop rbx ; pop arg count + magic
    shl rbx , 3 ; rbx = rbx * 8
    add rsp , rbx; pop args
 ;<end applic> 

	call write_sob_if_not_void

	mov rax, 0
	add rsp, 4*8
	pop rbp
	ret

